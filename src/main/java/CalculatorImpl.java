public class CalculatorImpl implements Calculator {
    public int suma(int a, int b) {
        return a + b;
    }

    public int diff(int a, int b) {
        return a - b;
    }

    public int mnozenie(int a, int b) {
        return a * b;
    }

    public int dzielenie(int a, int b) {
        return a / b;
    }
}
